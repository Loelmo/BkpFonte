@CPF_CNPJ VARCHAR(200),
@CEA_ESTADO_REGIAO INT,
@TX_SEXO_CONTATO VARCHAR(1),
@DATAINICIO DATETIME,
@DATAFIM DATETIME,
@NU_FUNCIONARIOS INT,
@TIPO_EMPRESA INT
------------------------------------------
)
As   
BEGIN     

SET NOCOUNT ON  

--Considera Respostas do Avaliador
DECLARE @bIS_AVALIADOR BIT
SET @bIS_AVALIADOR = 1
---------------------------------
--Considera Repostas da Empresa
DECLARE @bIS_EMPRESA BIT
SET @bIS_EMPRESA = 1
---------------------------------

SELECT
EC.CDA_EMP_CADASTRO,
QE.CDA_QUESTIONARIO_EMPRESA,
EC.TX_CPF_CNPJ,
EC.TX_RAZAO_SOCIAL,
--Possui Questionário de Responsabilidade Social----------       
ISNULL((SELECT QRESP.CDA_QUESTIONARIO_EMPRESA FROM TBL_QUESTIONARIO_EMPRESA QRESP WHERE QRESP.CEA_EMP_CADASTRO = EC.CDA_EMP_CADASTRO AND QRESP.FL_ATIVO = 1 AND QRESP.CEA_QUESTIONARIO = @CEA_QUESTIONARIO_RESPONSABILIDADE_SOCIAL AND QE.TX_PROTOCOLO is Not Null), 0) [PossuiResponsabilidadeSocial],
----------------------------------------------------------
--Possui do Questionário de Inovação------------        
ISNULL((SELECT QINOVA.CDA_QUESTIONARIO_EMPRESA FROM TBL_QUESTIONARIO_EMPRESA QINOVA WHERE QINOVA.CEA_EMP_CADASTRO = EC.CDA_EMP_CADASTRO AND QINOVA.FL_ATIVO = 1 AND QINOVA.CEA_QUESTIONARIO = @CEA_QUESTIONARIO_INOVACAO AND QE.TX_PROTOCOLO is Not Null), 0) [PossuiInovacao],
------------------------------------------------
--Verifica se a Empresa é Finalista do Questionário de Responsabilidade Social----------       
ISNULL((SELECT QFINAL.CDA_QUESTIONARIO_EMPRESA FROM TBL_QUESTIONARIO_EMPRESA QFINAL 
		JOIN TBL_ETAPA ETFINAL ON ETFINAL.CDA_ETAPA = QFINAL.CEA_ETAPA
		JOIN TBL_TIPO_ETAPA TIEFINAL ON TIEFINAL.CDA_TIPO_ETAPA = ETFINAL.CEA_TIPO_ETAPA 
