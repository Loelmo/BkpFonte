		AND QFINAL.FL_ATIVO = 1 
		AND QFINAL.CEA_QUESTIONARIO = @CEA_QUESTIONARIO_RESPONSABILIDADE_SOCIAL 
		AND QE.TX_PROTOCOLO is Not Null), 0) [FinalistaResponsabilidadeSocial],
----------------------------------------------------------
--Verifica se a Empresa é Finalista do Questionário de Inovação------------        
