ALTER PROCEDURE [dbo].[STP_SelecionaAdmGrupoPorNome]
(
 @sNome VARCHAR(200)
)
As    
BEGIN     

SET NOCOUNT ON  

  SELECT
		CDA_GRUPO,
		TX_GRUPO,
		TX_DESCRICAO,
		CEA_PROGRAMA
   FROM TBL_ADM_GRUPO
  WHERE TX_GRUPO like '%' + @sNome + '%';		
  
RETURN  

END  
    
